function corBackground(cor){

  if(cor == 'yellow'){
    document.body.style.backgroundColor = 'yellow';
  }
  if(cor == 'green'){
    document.body.style.backgroundColor = 'green';
  }
  if(cor == 'red'){
    document.body.style.backgroundColor = 'red';
  }
  if(cor == 'blue'){
    document.body.style.backgroundColor = 'blue';
  }
  
}